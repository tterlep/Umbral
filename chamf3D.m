a = (2-sqrt(2))*0.5;
b = 1-2*a;
c = sqrt(2) - 2*a-2*b;
dia1 = 1/(2*a+2*b+c);
dia2 = dia1;
% dia2 = 1/(2*a+2*b);
dia3 = 1/(2*a+3*b+2*c);

P = [...
    1 0 0;...
    0 1 0;...
    0 0 1;...
    dia1 dia1 0;...
    dia1 0 dia1; ...
    0 dia2 dia2;...
    dia3 dia3 dia3;...
    -1 0 0;...
    0 -1 0;...
    0 0 -1;...
    -dia1 dia1 0;...
    -dia1 0 dia1; ...
    0 -dia2 dia2;...
    -dia3 dia3 dia3;...
    dia1 -dia1 0;...
    dia1 0 -dia1; ...
    0 dia2 -dia2;...
    dia3 -dia3 dia3;...
    dia3 dia3 -dia3;...
    -dia1 -dia1 0;...
    -dia1 0 -dia1; ...
    0 -dia2 -dia2;...
    dia3 -dia3 -dia3;...
    -dia3 dia3 -dia3;...
    -dia3 -dia3 dia3;...
    -dia3 -dia3 -dia3;...
    ];

[k,vol] = convhulln(P);
P2 = circshift(P',1);
P2 = P2';
[k2,vol2] = convhulln(P2);
P3 = circshift(P2',1);
P3 = P3';
[k3,vol3] = convhulln(P3);

figure(1)
[x y z] = sphere(128);
h = surfl(x, y, z); 
set(h, 'FaceAlpha', 0.5)
shading flat
hold on
scatter3(P(:,1),P(:,2),P(:,3),50,'filled')
trisurf(k,P(:,1),P(:,2),P(:,3),'FaceColor','cyan')
hold off

